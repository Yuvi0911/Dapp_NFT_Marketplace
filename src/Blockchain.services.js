// is function ki help se metamask k wallet se connect kr skte h.
// jab bhi user phli baar connect wallet pr click krega ya humara metamask logout ho rhka hoga toh frontend se is function ki help metamask wallet se connection setup hoga.
const connectWallet = async () => {
    try {
        if(!ethereum){
            return alert('Please install Metamask');
        }
        // jo bhi account hum metamask me choose krege vo account mil jaiye ga 'eth_accounts ki help se
        const accounts = await ethereum.request({ method: 'eth_requestAccounts'});
        // connectedAccount me metamask me jo user connect h uska address store kr dega.
        setGlobalState('connectedAccount', accounts[0].toLowerCase());
    } catch (error) {
        reportError(error);
    }
}

// ye function frontend se tab call hoga jab user apna wallet change krega metamask se ya fir chain change hoga localhost se kisi aur chain jaise ethereum, amoy pr jaiye ge.
const isWalletConnected = async () => {
    try {
        if(!ethereum) {
            return alert('Please install metamask');
        }
            
            // jo bhi account hum metamask me choose krege vo account mil jaiye ga 'eth_accounts ki help se
            const accounts = await ethereum.request({method: 'eth_requestAccounts'});

            // jab hum chain ko change krege toh ye event fire hoga. for ex: hum localhost se kisi aur testnet ya mainnet pr change krte h jaise ki amoy, polygon, sepolia, goreli ethereum etc.
            // hum us chain ki id lege aur browser ko load kr dege.
            window.ethereum.on('chainChanged', async(chainId) => {
                window.location.reload();
            })

            // jab bhi hum metamsk me account change krege toh ye humari website me change kr dega ki account change hua h.
            window.ethereum.on('accountsChanged', async() => {
                setGlobalState('connectedAccount', accounts[0].toLowerCase())
                await isWalletConnected()
            })

            // accounts k andar user jis metamask account se connected h vo aa jaye ga aur yadi wallet se connect nhi h toh alert show ho jaiye ga.
            if(accounts.length){
                setGlobalState('connectedAccount', accounts[0].toLowerCase());
            }
            else{
                alert('Please connect wallet');
                console.log('No accounts found');
            }
    } catch (error) {
        reportError(error);
    }
}

